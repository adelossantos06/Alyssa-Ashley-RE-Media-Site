import { readdir, writeFile } from "node:fs/promises";
import path from "node:path";

const portfolioDir = path.resolve("public/images/portfolio");
const outputFile = path.resolve("src/portfolioImages.js");
const supportedExtensions = new Set([".jpg", ".jpeg", ".png", ".webp", ".avif"]);

async function getPortfolioFiles() {
    const entries = await readdir(portfolioDir, { withFileTypes: true });
    const files = [];

    for (const entry of entries) {
        if (entry.name.startsWith(".") || entry.name === "unused") {
            continue;
        }

        if (entry.isFile() && supportedExtensions.has(path.extname(entry.name).toLowerCase())) {
            files.push(entry.name);
            continue;
        }

        if (!entry.isDirectory()) {
            continue;
        }

        const projectEntries = await readdir(path.join(portfolioDir, entry.name), { withFileTypes: true });
        projectEntries
            .filter((projectEntry) => (
                projectEntry.isFile() &&
                !projectEntry.name.startsWith(".") &&
                supportedExtensions.has(path.extname(projectEntry.name).toLowerCase())
            ))
            .forEach((projectEntry) => {
                files.push(`${entry.name}/${projectEntry.name}`);
            });
    }

    return files.sort((a, b) => a.localeCompare(b, undefined, { numeric: true, sensitivity: "base" }));
}

const files = await getPortfolioFiles();

const fileContents = `// This file is generated by scripts/sync-portfolio.mjs.
// Add images to public/images/portfolio, then run npm run sync:portfolio.
export const portfolioImageFiles = ${JSON.stringify(files, null, 4)};
`;

await writeFile(outputFile, fileContents);

console.log(`Synced ${files.length} portfolio images.`);
